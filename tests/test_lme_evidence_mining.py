def test_lme_converter_mines_has_answer_into_turn_ids(tmp_path) -> None:
    """Mining has_answer messages must produce matching evidence_turn_ids."""
    converter = _load_converter()
    raw = [
        {
            "question_id": "has-ans-1",
            "question": "What did I graduate with?",
            "question_type": "single-session-user",
            "question_date": "2023/05/30",
            "answer": "Business Administration",
            "answer_session_ids": ["sess-grad"],
            "haystack_session_ids": ["sess-noise", "sess-grad"],
            "haystack_dates": ["2023-05-01", "2023-05-30"],
            "haystack_sessions": [
                [
                    {"role": "user", "content": "What's a good task app?"},
                    {"role": "assistant", "content": "Try Todoist."},
                ],
                [
                    {"role": "user", "content": "I graduated with a degree in Business Administration."},
                    {"role": "assistant", "content": "Congratulations!"},
                ],
            ],
        }
    ]
    # Patch the second message of the second session with has_answer=True
    # (this is the official LongMemEval evidence signal).
    raw[0]["haystack_sessions"][1][0]["has_answer"] = True
    in_path = tmp_path / "raw.json"
    out_path = tmp_path / "lme.json"
    in_path.write_text(json.dumps(raw, ensure_ascii=False), encoding="utf-8")
    n = converter.convert(in_path, out_path)
    assert n == 1
    case = json.loads(out_path.read_text(encoding="utf-8"))["cases"][0]
    # The mined turn id must reference the actual evidence message.
    assert case["evidence_turn_ids"] == ["sess-grad:m0"], (
        f"expected ['sess-grad:m0'] from has_answer mining, got {case['evidence_turn_ids']!r}"
    )
    # The session that contains the has_answer message must be in
    # evidence_session_ids, on top of the answer_session_ids entry.
    assert "sess-grad" in case["evidence_session_ids"]


