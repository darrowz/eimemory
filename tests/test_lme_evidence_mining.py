def test_lme_converter_propagates_evidence_turn_ids(tmp_path) -> None:
    """When raw carries top-level evidence_turn_ids, they must flow to eimemory."""
    converter = _load_converter()
    raw = [
        {
            "question_id": "syn-1",
            "question": "When did the user move?",
            "question_type": "temporal",
            "question_date": "2024-01-01",
            "answer": "March 2023",
            "answer_session_ids": ["sess-move"],
            "haystack_session_ids": ["sess-move", "sess-other"],
            "haystack_dates": ["2023-03-01", "2023-04-01"],
            "haystack_sessions": [
                [
                    {"role": "user", "content": "I moved in March 2023."},
                ],
                [
                    {"role": "user", "content": "I had dinner."},
                ],
            ],
            "evidence_turn_ids": ["sess-move:m0"],
        }
    ]
    in_path = tmp_path / "raw.json"
    out_path = tmp_path / "lme.json"
    in_path.write_text(json.dumps(raw, ensure_ascii=False), encoding="utf-8")
    n = converter.convert(in_path, out_path)
    assert n == 1
    out_data = json.loads(out_path.read_text(encoding="utf-8"))
    case = out_data["cases"][0]
    assert case["evidence_session_ids"] == ["sess-move"]
    assert "sess-move:m0" in case["evidence_turn_ids"]
    assert case["meta"]["evidence_mining_enabled"] is True


def test_lme_converter_feature_flag_disables_mining(tmp_path, monkeypatch) -> None:
    """With the flag off, evidence_turn_ids is hard-coded empty again."""
    converter = _load_converter()
    monkeypatch.setattr(converter, "_USE_REAL_EVIDENCE", False)
    raw = [
        {
            "question_id": "syn-2",
            "question": "q?",
            "question_type": "temporal",
            "answer": "a",
            "answer_session_ids": ["sess-x"],
            "haystack_session_ids": ["sess-x"],
            "haystack_dates": ["2024-01-01"],
            "haystack_sessions": [
                [{"role": "user", "content": "hi", "has_answer": True}],
            ],
        }
    ]
    in_path = tmp_path / "raw.json"
    out_path = tmp_path / "lme.json"
    in_path.write_text(json.dumps(raw, ensure_ascii=False), encoding="utf-8")
    converter.convert(in_path, out_path)
    case = json.loads(out_path.read_text(encoding="utf-8"))["cases"][0]
    # Flag off -> turn IDs dropped even though raw had has_answer.
    assert case["evidence_turn_ids"] == []
    assert case["meta"]["evidence_mining_enabled"] is False
    # Session-level evidence still flows (it predates the flag).
    assert case["evidence_session_ids"] == ["sess-x"]


def test_lme_converter_extract_real_evidence_helper() -> None:
    """Direct unit test of the extraction helper (no IO)."""
    converter = _load_converter()
    # Both top-level fields populated; helper unions them with dedup.
    ses, turns = converter._extract_real_evidence(
        {
            "answer_session_ids": ["A", "B"],
            "evidence_session_ids": ["B", "C"],
            "evidence_turn_ids": ["t1", "t2"],
        }
    )
    assert ses == ["A", "B", "C"]
    assert turns == ["t1", "t2"]
    # Only answer_session_ids.
    ses, turns = converter._extract_real_evidence({"answer_session_ids": ["only"]})
    assert ses == ["only"]
    assert turns == []
    # Empty case.
    assert converter._extract_real_evidence({}) == ([], [])
    # has_answer mining via the haystack path.
    ses, turns = converter._extract_real_evidence(
        {},
        haystack_session_ids=["s1"],
        haystack_sessions=[
            [
                {"role": "u", "content": "noise"},
                {"role": "u", "content": "evidence", "has_answer": True},
            ],
        ],
    )
    assert ses == ["s1"]
    assert turns == ["s1:m1"]
