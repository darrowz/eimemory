def test_lme_converter_emits_real_evidence(tmp_path) -> None:
    """A cleaned-variant raw case must yield non-empty evidence_session_ids."""
    converter = _load_converter()
    raw = _load_raw_sample(limit=1)
    assert raw[0].get("answer_session_ids"), "raw sample missing answer_session_ids"
    in_path = tmp_path / "raw.json"
    out_path = tmp_path / "lme.json"
    in_path.write_text(json.dumps(raw, ensure_ascii=False), encoding="utf-8")
    n = converter.convert(in_path, out_path)
    assert n == 1
    out_data = json.loads(out_path.read_text(encoding="utf-8"))
    case = out_data["cases"][0]
    assert case["evidence_session_ids"], "expected non-empty evidence_session_ids"
    # The answer session must be a subset of evidence_session_ids.
    assert set(raw[0]["answer_session_ids"]).issubset(set(case["evidence_session_ids"]))
    # Real turn evidence must be present for the cleaned variant when
    # the raw case has has_answer=True messages. Case 0 has 2.
    assert "evidence_turn_ids" in case
    assert len(case["evidence_turn_ids"]) >= 1, (
        f"expected >= 1 evidence_turn_ids mined from has_answer, got {case['evidence_turn_ids']!r}"
    )
    # Turn IDs must follow the {sid}:m{msg_idx} format the converter
    # also emits on the haystack side — otherwise the adapter's
    # set-membership check will silently miss every hit.
    import re
    turn_id_pattern = re.compile(r"^.+:m\d+$")
    for turn_id in case["evidence_turn_ids"]:
        assert turn_id_pattern.match(turn_id), (
            f"turn_id {turn_id!r} is not in {{sid}}:m{{msg_idx}} format"
        )


def test_lme_converter_evidence_matches_raw(tmp_path) -> None:
    """The eimemory evidence_session_ids must include all raw answer_session_ids."""
    converter = _load_converter()
    raw = _load_raw_sample(limit=3)
    in_path = tmp_path / "raw.json"
    out_path = tmp_path / "lme.json"
    in_path.write_text(json.dumps(raw, ensure_ascii=False), encoding="utf-8")
    n = converter.convert(in_path, out_path)
    assert n == 3
    out_data = json.loads(out_path.read_text(encoding="utf-8"))
    for raw_case, eim_case in zip(raw, out_data["cases"]):
        assert set(raw_case.get("answer_session_ids") or []).issubset(
            set(eim_case["evidence_session_ids"])
        ), (
            f"raw answer_session_ids missing from eimemory evidence_session_ids "
            f"for case {raw_case['question_id']}"
        )
        # The eimemory case_id is the raw question_id.
        assert eim_case["case_id"] == raw_case["question_id"]
        # The eimemory question mirrors the raw question.
        assert eim_case["question"] == raw_case["question"]


