"""Tests for LongMemEval evidence mining in the converter.

Background
----------

The previous converter hard-coded ``evidence_turn_ids = []`` and only
populated ``evidence_session_ids`` from ``answer_session_ids``. That made
the LME eval a *ranking* test (any session in the answer set was a hit)
rather than a *retrieval* test (did we surface the specific turns the
answer cites?).

The new converter mines the per-message ``has_answer`` boolean — the
official LongMemEval evidence signal — and turns it into
``evidence_turn_ids`` of the form ``{session_id}:m{msg_idx}`` so the
adapter's set-membership check works end-to-end. It also unions in
``answer_session_ids`` and the top-level ``evidence_session_ids`` /
``evidence_turn_ids`` fields when present (other LongMemEval variants
publish those).

These tests pin:

* ``has_answer`` messages on the raw side produce matching
  ``evidence_turn_ids`` on the eimemory side, for the cleaned variant
  and for synthetic inputs.
* ``answer_session_ids`` continues to flow into
  ``evidence_session_ids`` (no regression on the original wiring).
* The feature flag controls behaviour; disabling it reproduces the
  pre-fix empty-turn-ids behaviour.

The ``data/longmemeval_s_eimemory.json`` snapshot is NOT regenerated by
these tests; the converter is exercised against the raw input only.
"""
from __future__ import annotations

import importlib.util
import json
import sys
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parent.parent
CONVERTER_PATH = REPO_ROOT / "scripts" / "convert_longmemeval_to_eimemory.py"
DATA_PATH = REPO_ROOT / "data" / "longmemeval_s_cleaned.json"


def _load_converter():
    """Import the converter script as a module (it is in scripts/, not a package)."""
    spec = importlib.util.spec_from_file_location("lme_converter", CONVERTER_PATH)
    module = importlib.util.module_from_spec(spec)
    sys.modules[spec.name] = module
    assert spec.loader is not None
    spec.loader.exec_module(module)
    return module


def _load_raw_sample(limit: int = 1) -> list[dict]:
    return _load_json_array_sample(DATA_PATH, limit=limit)


def _load_json_array_sample(path: Path, *, limit: int | None = None) -> list[dict]:
    items: list[dict] = []
    for item in _iter_json_array(path):
        items.append(item)
        if limit is not None and len(items) >= limit:
            break
    return items


def _iter_json_array(path: Path):
    decoder = json.JSONDecoder()
    buffer = ""
    started = False
    with path.open("r", encoding="utf-8") as handle:
        while True:
            chunk = handle.read(1024 * 1024)
            if chunk:
                buffer += chunk
            while True:
                buffer = buffer.lstrip()
                if not started:
                    if not buffer:
                        break
                    if buffer[0] != "[":
                        raise ValueError(f"expected JSON array in {path}")
                    buffer = buffer[1:]
                    started = True
                    continue
                if not buffer:
                    break
                if buffer[0] == ",":
                    buffer = buffer[1:]
                    continue
                if buffer[0] == "]":
                    return
                try:
                    item, end = decoder.raw_decode(buffer)
                except json.JSONDecodeError:
                    if chunk:
                        break
                    raise
                yield item
                buffer = buffer[end:]
            if not chunk:
                return


